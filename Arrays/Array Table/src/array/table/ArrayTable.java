
package array.table;

public class ArrayTable {

 
    public static void main(String[] args) {
        System.out.println("Index\tValue");
        int numbers[] = {15,13,77,144,22};
        
        for(int i = 0; i<numbers.length; i++){
            System.out.println(i + "\t" + numbers[i]);
        }
            
    }
}
